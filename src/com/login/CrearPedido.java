    private void jLGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLGuardarMouseClicked
        String clienteNombre = txNombreCliente.getText(); 
        String clienteNumero = txNumeroCliente.getText();
        String clienteDireccion = txDireccionCliente.getText(); 
        
        Pedido.ConsultarGuardarPedido();
        ConexionSQL.GuardarCliente(InsertarCliente, clienteNombre, clienteNumero, clienteDireccion);
        
        double total = 0;
        double pago = Double.parseDouble(txPago.getText());
        double cambio;        
        String[][] datosdelpedido = ExtraerDatos();
        double[] PedidoPrecio = new double[datosdelpedido.length];
        
        for (int i = 0; i < datosdelpedido.length; i++) {
            PedidoPrecio[i] = Double.parseDouble(datosdelpedido[i][3]);
        }
        for (double num : PedidoPrecio) {
            total += num;
        }  
        cambio = pago - total;  
        TxTotal.setText("Total: " + String.valueOf(total));
        txCambio.setText(String.valueOf(cambio));
        
        Impresion.ImprimirTicket(clienteNombre, clienteNumero, clienteDireccion, total, pago, cambio, datosdelpedido);
        
        JOptionPane.showMessageDialog(null, "Pedido guardado" , "operacion", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_jLGuardarMouseClicked

